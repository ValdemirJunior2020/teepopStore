const defaultSizes = ["S", "M", "L", "XL", "2XL"];

function buildVariants(productKey, style = "Unisex Tee") {
  return defaultColors.flatMap((color) =>
    defaultSizes.map((size) => ({
      sku: `TEEPOP-${productKey.toUpperCase().replace(/[^A-Z0-9]+/g, "-")}-${color.name
        .toUpperCase()
        .replace(/\s+/g, "")}-${size}`,
      style,
      color: color.name,
      colorHex: color.hex,
      size,
      price: TSHIRT_PRICE,
      stock: 20,
      active: true
    }))
  );
}

